'''Con el metodo 'tuple()', podemos convertir una
lista en una tupla. Y de la misma manera con el 
metodo 'list()', podemos convertir una tupla en 
una lista'''

Lista = ['Atletismo', 'Natación', 'Futbol', 'Gimnasia', 'Tenis']
Tupla = tuple(Lista)
print(Tupla)


Tupla2 = ['Atletismo', 'Natación', 'Futbol', 'Gimnasia', 'Tenis']
Lista2 = list(Tupla2)
print(Lista2)