#Con el simbolo de '+' podemos concatear también palabras o frases
Palabra1 = "Así podemos"
Palabra2 = "concatenar frases"

#En una nueva variable concatenamos las frases para formar una sola
Frase = Palabra1 + ' ' + Palabra2

#Imprimimos la frase resultante
print(Frase)

#También podemos con la función 'print()' imprimir sin necesidad de guardar en una variable
